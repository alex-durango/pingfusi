#!/usr/bin/env node
// motion-apply.js — `pingfusi motion apply-sampled <name> <motion-id>`: replay the item's
// SAMPLED tracks into the clone as a small self-contained WAAPI player.
//
// The sampled tier's middle step. `motion sample` (harness/motion-sampler.js) recorded
// the live page's motion as sampled-tier tracks in targets/<name>/motion-doc.json —
// computed values at uniform VIRTUAL-time offsets, fps receipted in provenance.source.
// This command turns that record into the clone's own animation implementation:
// clone/motion-replay.js, bound by selector and started per the track's trigger
// semantics (load → immediately; scroll-triggered → an IntersectionObserver on the item's
// scope). The replay is TIME-BASED: a site animating px-per-rAF-frame was recorded
// honestly at the declared fps, and the WAAPI replay NORMALIZES that frame-rate
// dependence — receipted in the player's header, never hidden.
//
// A CLIP IS NOT AN IMPLEMENTATION FOR ONGOING MOTION. A finite (settling) track replays
// as its verbatim clip — one pass, then RELEASE: fill "none" with an explicit onfinish
// that commits the final frame ONLY when no other writer owns the element, then cancels
// (a finished clip must never keep squatting on the element the way a permanent
// fill:"forwards" would — seen live: a 4s belt clip's forwards fill silently overrode a
// coexisting implementation forever). An ONGOING track (no settle observed — see the
// sampler's trackIsOngoing) is REFUSED as a clip: a clip that stops is a fabricated
// ending. It ships only when a lifted periodic-class fit (marquee) supplies the looping
// form, and then the player emits the LOOP — WAAPI iterations Infinity driven by the
// fit's velocity/direction, with the wrap distance measured from the element at RUNTIME
// (the duplicated-content scrollWidth/2 pattern), never from the clip.
//
// ONE OWNER. Before anything is written, an owner probe samples the target elements'
// inline style + computed transform for ~1s in the served clone (the kit's own replay
// held aside), each viewport group of elements SCROLLED INTO VIEW first — competing
// writers are commonly visibility-gated, and a probe watching from scroll 0 clears a
// belt whose writer paused off-screen (seen live). Any change means another
// implementation already writes those elements, and the apply REFUSES with a receipt
// naming the selector — resolving ownership is the operator's call, the kit never
// stacks implementations.
//
// This file is the CLONE'S animation implementation, not instrument contamination: the
// instrument doctrine ("never bake yourself into artifacts") bars the CAPTURE tooling
// from the clone; the replay IS the cloned motion, exactly like a hand-written
// fixes.js — generated so the sampled record stays the single source of truth.
//
// Idempotent by marker: the <script> tag lives between pingfusi:motion-replay markers in
// clone/index.html; a re-run replaces ONLY its own block, never other markup. Re-running
// for a second item regenerates motion-replay.js with every applied item's tracks, so no
// apply can silently drop another item's replay.
//
// Receipts for every intervention AND refusal: targets/<name>/motion/<id>/
// apply-sampled.json plus a workflow.jsonl entry; the player itself carries a header
// receipt (schema pingfusi/motion-replay@2, doc + track fingerprints). Item status →
// "applied-sampled" (NON-terminal): `pingfusi motion verify-sampled` owns the walk to
// "verified-sampled".
//
// Quarantine: only a DECLARED item may be applied — sweep candidates stay advisory, and
// nothing here runs on an ordinary clone.
//
// usage: pingfusi motion apply-sampled <name> <motion-id> [--attach <port|host:port>]
//          [--chrome <path>] [--headful]
